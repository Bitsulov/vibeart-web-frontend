/**
 * Описывает состояние отдельного компонента серверной инфраструктуры.
 *
 * - `"good"` — все системы работают штатно.
 * - `"problem"` — деградация производительности или частичный сбой.
 * - `"bad"` — сервис недоступен.
 */
export type ServerStatusType = "good" | "problem" | "bad";

/**
 * Глобальная конфигурация приложения, хранимая в хранилище Redux (слайс `app`).
 *
 * Содержит настройку языка интерфейса и сведения о состоянии серверных
 * сервисов, получаемые при инициализации приложения.
 */
export interface AppConfigType {
    /** Код языка текущей локали по стандарту BCP 47 (например, `"ru"`, `"en"`). */
    currentLanguage: string;
    /** Общее состояние серверного API. */
    serverStatus: ServerStatusType;
    /** Количество диалогов с непрочитанными сообщениями. */
    unreadChatsCount: number;
    /** Количество непрочитанных уведомлений. */
    unreadNotificationsCount: number;
}
