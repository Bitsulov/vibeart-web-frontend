/**
 * Описывает тег — метку, которую можно прикрепить
 * к публикации для удобства поиска и фильтрации контента.
 */
export interface TagType {
    /** Человекочитаемое название тега (например, «природа», «портрет»). */
    title: string;
    /** Дата и время создания тега в формате ISO 8601. */
    createdAt: string;
}
