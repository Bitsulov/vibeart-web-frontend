/**
 * Описывает одно сообщение в рамках личного диалога между двумя
 * пользователями сайта.
 */
export interface MessageType {
    /** Текстовое содержимое сообщения. */
    text: string;
    /** Дата и время отправки сообщения в формате ISO 8601. */
    createdAt: string;
    /** Признак того, что сообщение отправлено текущим авторизованным пользователем. */
    isYour: boolean;
    /**
     * Признак того, что сообщение получено после начала текущей сессии.
     * Присутствует только у сообщений, поступивших пока окно диалога открыто.
     */
    isNew?: boolean;
    /**
     * Статус доставки и прочтения:
     * - `"save"` — поставлено в очередь локально, ещё не передано на сервер;
     * - `"sent"` — доставлено на сервер, получатель ещё не прочитал;
     * - `"read"` — открыто и прочитано получателем.
     */
    status: "save" | "sent" | "read"
}
